int getTxPowerScale(int txpower)
{
	int intVal = 0;
	if(txpower == 100)
		intVal = 0;
	else if(txpower >= 88)
		intVal = 1;
	else if(txpower >= 50)
		intVal = 6;
	else if(txpower >= 25)
		intVal = 12;
	else if (txpower >= 1)
		intVal = 17;
	else
		intVal = 99; // Special case. Reduce txpower to 1.
	return intVal;
}

