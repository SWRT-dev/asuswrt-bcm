    uint16_t crc = 0;

    if (!dev)
        dev = dev_get_by_name(&init_net, "eth0");
    if (!dev)
    {
       printk("test error %s:%d\n",__FUNCTION__,__LINE__);
       goto error;
    }
   
    skb = netdev_alloc_skb(dev, packet_size + sizeof(crc) );
    if (!skb)
    {
        printk("test error %s:%d\n",__FUNCTION__,__LINE__);
        goto error;
    }
    skb_put(skb, packet_size + sizeof(crc) );
    memcpy(skb->data, data, L2_HEADER_SIZE);
    memcpy(&skb->data[L2_HEADER_SIZE] , &data[L2_HEADER_SIZE], packet_size - L2_HEADER_SIZE); 
    skb->data[packet_size ] = crc;
    for (i = 0; i < inter; i++)
    {
        memcpy(&skb->data[L2_HEADER_SIZE] , &data[L2_HEADER_SIZE + i % sizeof(data)], packet_size - L2_HEADER_SIZE);
        crc = 0;
        rc = enet_xmit(skb, dev);
        if (i % 50 == 0)
            printk(" sent pckt[%d] \n", i);
        if (rc)
        {
            printk("test error %s:%d\n",__FUNCTION__,__LINE__);  
            goto error;      
        }

         while(!rc)
            rc = qemu_enet_rx_pkt(1);
        if (i % 50 == 0)
            printk(" recieved pckt[%d]\n", i);
    }
  
  return 0;

